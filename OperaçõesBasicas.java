public class OperaçõesBasicas {
    public static void main(String[] args) {
        
        //Declarar variaveis
        int n = 4;
        int m = 2;

        double soma = n + m;
        double subtração = n - m;
        double multiplicação = n * m;
        double divisão = n/m;

        System.out.println("A soma é: " + soma);
        System.out.println("A subtração é: " + subtração);
        System.out.println("A multiplicação é: " + multiplicação);
        System.out.println("A divisão é: " + divisão);
    }
}
